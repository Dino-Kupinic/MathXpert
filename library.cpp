#include "library.h"

namespace mathX {
    double basic::add(double a, double b) {
        return a + b;
    }

    double basic::sub(double a, double b) {
        return a - b;
    }

    double basic::mul(double a, double b) {
        return a * b;
    }

    double basic::div(double a, double b) {
        return a / b;
    }
}
